int networkDelayTime(std::vector<std::vector<int>> times, int n, int k) {
  std::unordered_map<int, std::vector<std::pair<int, int>>> graph;
  for (auto time : times) {
    graph[time[0]].emplace_back(std::make_pair(time[1], time[2]));
  }

  std::vector<int> fastest(n + 1, INT_MAX);
  auto comp = [](const std::pair<int, int>& a, const std::pair<int, int>& b) {
    return a.second > b.second;
  };
  std::priority_queue<std::pair<int, int>, std::vector<std::pair<int, int>>, decltype(comp)> queue{
      comp};
  queue.push(std::make_pair(k, 0));
  fastest[k] = 0;
  while (!queue.empty()) {
    int node = queue.top().first;
    int distance = queue.top().second;
    queue.pop();

    if (distance == fastest[node]) {
      for (auto edge : graph[node]) {
        int target = edge.first;
        int weight = edge.second;

        int distanceToTarget = distance + weight;
        if (distanceToTarget < fastest[target]) {
          fastest[target] = distanceToTarget;
          queue.push(std::make_pair(target, distance + weight));
        }
      }
    }
  }

  int maxDistance = -1;
  for (int i = 1; i <= n; i++) {
    int distance = fastest[i];
    if (distance > maxDistance) {
      maxDistance = distance;
    }
  }

  if (maxDistance == INT_MAX) {
    return -1;
  }

  return maxDistance;
}

