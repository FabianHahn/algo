using ::testing::IsEmpty;
using ::testing::UnorderedElementsAre;

std::vector<std::vector<int>> threeSum(std::vector<int>& nums) {
  std::unordered_multiset<int> numbers;
  numbers.insert(nums.begin(), nums.end());

  std::set<std::pair<int, int>> triplets;
  for (auto it = numbers.begin(); it != numbers.end(); ) {
    int a = *it;
    for (auto it2 = numbers.begin(); it2 != numbers.end(); ) {
      int b = *it2;
      if (a == b && numbers.count(b) == 1) {
        do {
          ++it2;
        } while (it2 != numbers.end() && *it2 == b);
        continue;
      }

      int required = -a - b;
      int numRequired = 1;
      if (a == required) {
        numRequired++;
      }
      if (b == required) {
        numRequired++;
      }

      if (numbers.count(required) >= numRequired) {
        std::vector<int> triplet{a, b, required};
        std::sort(triplet.begin(), triplet.end());

        triplets.insert(std::make_pair(triplet[0], triplet[1]));
      }

      do {
        ++it2;
      } while (it2 != numbers.end() && *it2 == b);
    }

    do {
      ++it;
    } while (it != numbers.end() && *it == a);
  }

  std::vector<std::vector<int>> result;
  for (auto triplet : triplets) {
    result.emplace_back(
        std::vector<int>{triplet.first, triplet.second, -triplet.first - triplet.second});
  }

  return result;
}

TEST(ThreeSum, triplets1) {
  std::vector<int> input{-1, 0, 1, 2, -1, -4};
  EXPECT_THAT(
      threeSum(input),
      UnorderedElementsAre(UnorderedElementsAre(-1, -1, 2), UnorderedElementsAre(-1, 0, 1)));
}

TEST(ThreeSum, triplets2) {
  std::vector<int> input{0, 1, 1};
  EXPECT_THAT(threeSum(input), IsEmpty());
}

